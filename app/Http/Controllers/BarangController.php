    {
        $isidata = $request->validate([
            'namabarang'        => 'required',
            'harga'             => 'required',
            'deskripsi'         => 'required',
            'stock'             => 'required',
            'categori_id'       => 'required'
        ]);
        Barang::create($isidata);
        return redirect('/barang')->with('informasi','Data barang berhasil di simpan');
    }

